function createPatternCards() {
    const patternCardsContainer = document.getElementById('pattern-cards');
    patterns.forEach(pattern => {
        if (pattern.showcase) { // showcase true  
            const card = document.createElement('div');
            card.className = 'pattern-card';

            const title = document.createElement('h3');
            title.innerText = pattern.name;

            const imageContainer = document.createElement('div');
            imageContainer.className = `image-container ${pattern.layout}`; //     

            pattern.images.forEach(imgSrc => {
                const img = document.createElement('img');
                img.src = imgSrc;
                imageContainer.appendChild(img);
            });

            const scoreText = document.createElement('p');
            scoreText.innerHTML = `: <span class="score">${pattern.score}</span>`;

            card.appendChild(title);
            card.appendChild(imageContainer);
            card.appendChild(scoreText);

            patternCardsContainer.appendChild(card);
        }
    });
}

